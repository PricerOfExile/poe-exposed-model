def rarity_array(data):
    rarity = data['rarity']
    if rarity == "Normal":
        return [1, 0, 0]
    elif rarity == "Magic":
        return [0, 1, 0]
    elif rarity == "Rare":
        return [0, 0, 1]
    else:
        return [0, 0, 0]


def socket_array(data):
    socket = data['socket']
    if socket == 'N':
        return [1, 0, 0, 0, 0, 0]
    elif socket == 'A':
        return [0, 1, 0, 0, 0, 0]
    elif socket == 'B':
        return [0, 0, 1, 0, 0, 0]
    elif socket == 'R':
        return [0, 0, 0, 1, 0, 0]
    elif socket == 'G':
        return [0, 0, 0, 0, 1, 0]
    elif socket == 'W':
        return [0, 0, 0, 0, 0, 1]
    else:
        return [0, 0, 0, 0, 0, 0]


def identified_array(data):
    identified = data['identified']
    if identified:
        return [1, 0]
    else:
        return [0, 1]


def influence_array(data):
    influences = data['influences']
    return [
        1 if influences['warlord'] else 0,
        1 if influences['hunter'] else 0,
        1 if influences['redeemer'] else 0,
        1 if influences['crusader'] else 0,
        1 if influences['shaper'] else 0,
        1 if influences['elder'] else 0,
    ]


